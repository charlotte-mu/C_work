// hw2_16, 沒有編排的程式
#include <iostream>
#include <cstdlib>
using namespace std;
int main(void)
{
    int a,b;
    a=2;
    b=6;
    cout << "a=" << a << endl;
    cout << "b=" << b << endl;
    cout << a << "+" << b << "=" << a+b << endl;
    system("pause");
    return 0;
}

/* output-----
a=2
b=6
2+6=8
------------*/
