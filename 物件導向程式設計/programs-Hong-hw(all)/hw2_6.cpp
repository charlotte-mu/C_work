#include <iostream>   	// 含括iostream檔案
#include <cstdlib>		// 含括cstdlib檔案
using namespace std;    // 使用std名稱空間 
int main(void)
{   
    int n1,n2;          // 宣告整數變數n1,n2     
    n1=6;               // n1設值為6 
    n2=8;               // n2設值為8
    cout << "n1=" << n1 << endl;   // 印出變數內容
    cout << "n2=" << n2 << endl;   // 印出變數內容
    system("pause");
    return 0;
}

